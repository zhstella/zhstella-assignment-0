# Python script to add two numbers and print the result

def add_two_numbers(num1, num2):
    return num1 + num2

if __name__ == "__main__":
    # Example numbers to add
    num1 = 3
    num2 = 5
    result = add_two_numbers(num1, num2)
    print(f"The sum of {num1} and {num2} is: {result}")
